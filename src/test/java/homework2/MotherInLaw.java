package homework2;

public class MotherInLaw {

    private int sonCount;
    private boolean loveForDaughter;
    private double weight;
    private String favoriteWords ;
    private boolean beliefInHoroscope;


    public int getSonCount() {
        return sonCount;
    }

    public void setSonCount(int sonCount) {
        this.sonCount = sonCount;
    }

    public boolean isLoveForDaughter() {
        return loveForDaughter;
    }

    public void setLoveForDaughter(boolean loveForDaughter) {
        this.loveForDaughter = loveForDaughter;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getFavoriteWords() {
        return favoriteWords;
    }

    public void setFavoriteWords(String favoriteWords) {
        this.favoriteWords = favoriteWords;
    }

    public boolean IsBeliefInHoroscope() {
        return beliefInHoroscope;
    }

    public void setBeliefInHoroscope(boolean beliefInHoroscope) {
        this.beliefInHoroscope = beliefInHoroscope;
    }
}
