package homework2;

public class Mouse {

    private String color;
    private boolean comfort;
    private int clickBySecond;
    private long clickByOneYear;
    private double weight;


    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public boolean IsComfort() {
        return comfort;
    }

    public void setIsComfort(boolean comfort) {
        this.comfort = comfort;
    }

    public int getClickBySecond() {
        return clickBySecond;
    }

    public void setClickBySecond(int clickBySecond) {
        this.clickBySecond = clickBySecond;
    }

    public long getClickByOneYear() {
        return clickByOneYear;
    }

    public void setClickByOneYear(long clickByOneYear) {
        this.clickByOneYear = clickByOneYear;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
