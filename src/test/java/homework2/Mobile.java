package homework2;

public class Mobile {

    private String label;
    private boolean gift;
    private double chargeTime;
    private String chargeTimeMeasure;
    private boolean iOS;


    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public boolean isGift() {
        return gift;
    }

    public void setIsGift(boolean gift) {
        this.gift = gift;
    }

    public double getChargeTime() {
        return chargeTime;
    }

    public void setChargeTime(double chargeTime) {
        this.chargeTime = chargeTime;
    }

    public String getChargeTimeMeasure() {
        return chargeTimeMeasure;
    }

    public void setChargeTimeMeasure(String chargeTimeMeasure) {
        this.chargeTimeMeasure = chargeTimeMeasure;
    }

    public boolean getIsiOS() {
        return iOS;
    }

    public void setIsiOS(boolean iOS) {
        this.iOS = iOS;
    }
}
